var MinStack = function () {
  this.stack = [];
  this.minValues = [];
};

/**
 * @param {number} val
 * @return {void}
 */
MinStack.prototype.push = function (val) {
  this.stack.push(val);
  this.minValues.push(Math.min(val, this.getMin()));
};

/**
 * @return {void}
 */
MinStack.prototype.pop = function () {
  this.minValues.pop();
  return this.stack.pop();
};

/**
 * @return {number}
 */
MinStack.prototype.top = function () {
  return this.stack[this.stack.length - 1];
};

/**
 * @return {number}
 */
MinStack.prototype.getMin = function () {
  return this.minValues[this.minValues.length - 1] ?? Infinity;
};

